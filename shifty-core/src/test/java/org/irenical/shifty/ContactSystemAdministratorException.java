package org.irenical.shifty;

public class ContactSystemAdministratorException extends Exception {

  private static final long serialVersionUID = 1L;

  public ContactSystemAdministratorException() {
    super();
  }

  public ContactSystemAdministratorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

  public ContactSystemAdministratorException(String message, Throwable cause) {
    super(message, cause);
  }

  public ContactSystemAdministratorException(String message) {
    super(message);
  }

  public ContactSystemAdministratorException(Throwable cause) {
    super(cause);
  }

}
